<p>Arnouse Digital Devices Corporation (ADDC) is a privately held company.</p>
<p>It was founded in 2002 by Michael Arnouse to create a low powered interchangeable compute platform allowing users an unprecedented level of flexibility with ultra-high security in a portable micro form factor.</p>
<p>ADDC has been granted 18 utility patents, with three pending in the areas of pluggable computing and its surrounding ecosystem, which cover all aspects of ADDC products and technology.</p>
